@dataclass
class MarketEvent:
    """Market data update for a single symbol and day."""

    time: pd.Timestamp
    price: float


@dataclass
class SignalEvent:
    """Trading signal generated by a strategy."""

    time: pd.Timestamp
    signal: float  # -1 short, 0 flat, +1 long


@dataclass
class OrderEvent:
    """Order to adjust position to a target signal."""

    time: pd.Timestamp
    target_position: float


@dataclass
class FillEvent:
    """Fill confirming execution at a given price."""

    time: pd.Timestamp
    position: float
    price: float


class EventQueue:
    """FIFO queue for events in the backtest."""

    def __init__(self) -> None:
        self._queue: deque=deque()

    def put(self, event: object) -> None:
        """Append a new event to the queue."""
        self._queue.append(event)

    def get(self) -> object | None:
        """Pop the next event or return None if the queue is empty."""
        if self._queue:
            return self._queue.popleft()
        return None

    def empty(self) -> bool:
        """Return True when there are no more events."""
        return not self._queue


class HistoricalDataHandler:
    """Stream daily close prices as MarketEvent instances."""

    def __init__(
        self,
        symbol: str="EURUSD",
        start: str | None=None,
        end: str | None=None,
    ) -> None:
        self.symbol = symbol
        panel = load_eod_panel().astype(float)
        prices = panel[self.symbol].dropna()

        start_ts = pd.to_datetime(start) if start is not None else None
        end_ts = pd.to_datetime(end) if end is not None else None
        if start_ts is not None or end_ts is not None:
            prices = prices.loc[start_ts:end_ts]

        self.prices = prices
        self.index = self.prices.index
        self.pointer = 0

    def have_more_bars(self) -> bool:
        """True while there are bars left to stream."""
        return self.pointer < len(self.index)

    def stream_next(self, events: EventQueue) -> None:
        """Push the next MarketEvent to the event queue."""
        if not self.have_more_bars():
            return
        ts = self.index[self.pointer]
        price = float(self.prices.iloc[self.pointer])
        events.put(MarketEvent(time=ts, price=price))
        self.pointer += 1


class SmaCrossStrategy:
    """Simple moving-average crossover strategy."""

    def __init__(
        self,
        fast_window: int=20,
        slow_window: int=100,
    ) -> None:
        self.fast_window = fast_window
        self.slow_window = slow_window
        self.prices: list[float]=[]
        self.times: list[pd.Timestamp]=[]
        self.current_signal: float=0.0

    def on_market_event(
        self,
        event: MarketEvent,
        events: EventQueue,
    ) -> None:
        """Update rolling averages and emit signals when they cross."""
        self.times.append(event.time)
        self.prices.append(event.price)
        series = pd.Series(self.prices, index=self.times)

        fast = series.rolling(self.fast_window).mean()
        slow = series.rolling(self.slow_window).mean()

        if len(series) < self.slow_window:
            return  # not enough data yet

        if fast.iloc[-1] > slow.iloc[-1] and self.current_signal <= 0.0:
            self.current_signal = 1.0
            events.put(
                SignalEvent(
                    time=event.time,
                    signal=self.current_signal,
                )
            )
        elif fast.iloc[-1] < slow.iloc[-1] and self.current_signal >= 0.0:
            self.current_signal = -1.0
            events.put(
                SignalEvent(
                    time=event.time,
                    signal=self.current_signal,
                )
            )


class NaivePortfolio:
    """Track positions and convert signals into fills."""

    def __init__(self) -> None:
        self.position: float=0.0  # current position in units of capital
        self.positions: list[float]=[]
        self.times: list[pd.Timestamp]=[]

    def on_signal(
        self,
        event: SignalEvent,
        events: EventQueue,
    ) -> None:
        """Create an order that moves to the target signal."""
        events.put(
            OrderEvent(
                time=event.time,
                target_position=event.signal,
            )
        )

    def on_fill(self, event: FillEvent) -> None:
        """Update internal position records from a fill."""
        self.position = event.position
        self.times.append(event.time)
        self.positions.append(self.position)

    def get_position_series(self) -> pd.Series:
        """Return the full position series indexed by time."""
        return pd.Series(self.positions, index=self.times)


class SimulatedExecutionHandler:
    """Turn orders into immediate fills at the closing price."""

    def __init__(self) -> None:
        self.last_price: float | None=None

    def on_market_event(self, event: MarketEvent) -> None:
        """Record the most recent price for use in fills."""
        self.last_price = event.price

    def on_order(
        self,
        event: OrderEvent,
        events: EventQueue,
    ) -> None:
        """Generate a fill at the latest known price."""
        if self.last_price is None:
            return
        events.put(
            FillEvent(
                time=event.time,
                position=event.target_position,
                price=self.last_price,
            )
        )


def format_pct(value: float) -> str:
    """Format a decimal fraction as a percentage string."""
    return f"{value * 100.0: .2f}\\%"  # percentage with two decimals


def format_float(value: float, decimals: int=2) -> str:
    """Format a floating-point number with fixed decimals."""
    fmt = f"{{: .{decimals}f}}"
    return fmt.format(value)


