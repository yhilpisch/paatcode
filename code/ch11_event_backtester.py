def plot_equity_curves(
    net_bh: pd.Series,
    net_sma: pd.Series,
    symbol: str,
    outfile: str="figures/ch11_event_sma_vs_bh.pdf",
) -> None:
    """Plot equity curves for buy-and-hold and event-based SMA."""
    wealth_bh = (1.0 + net_bh).cumprod()
    wealth_sma = (1.0 + net_sma).cumprod()

    wealth_bh = wealth_bh / wealth_bh.iloc[0]
    wealth_sma = wealth_sma / wealth_sma.iloc[0]

    fig, ax = plt.subplots(figsize=(7.0, 3.2))
    cmap = plt.cm.coolwarm

    w_min = float(min(wealth_bh.min(), wealth_sma.min()))
    w_max = float(max(wealth_bh.max(), wealth_sma.max()))
    padding = 0.05 * (w_max - w_min) if w_max > w_min else 0.02

    ax.plot(
        wealth_bh.index,
        wealth_bh.values,
        label=f"buy-and-hold ({symbol})",
        color=cmap(0.10),
        lw=1.0,
        ls=":",
    )
    ax.plot(
        wealth_sma.index,
        wealth_sma.values,
        label="event-based SMA strategy",
        color=cmap(0.90),
        lw=1.0,
    )

    ax.set_ylabel("wealth (normalised)")
    ax.set_xlabel("date")
    ax.set_title(f"{symbol}: event-based SMA vs buy-and-hold")
    ax.legend(loc="upper left")
    ax.set_ylim(
        max(0.0, w_min - padding),
        w_max + padding,
    )
    ax.grid(True, alpha=0.3)

    fig.tight_layout()
    fig.savefig(outfile, bbox_inches="tight")
    plt.close(fig)


def count_trades(position: pd.Series) -> int:
    """Count the number of trades implied by a position series."""
    pos = position.fillna(0.0)
    delta_q = pos.diff().fillna(pos)
    return int((delta_q != 0.0).sum())


def main(
    symbol: str="EURUSD",
    fast_window: int=20,
    slow_window: int=100,
    cost: float=0.0005,
    fin: float=0.0001,
    leverage: float=1.0,
    start: str | None=None,
    end: str | None=None,
) -> None:
    """Run event-based SMA backtest and create figures and macros."""
    events = EventQueue()
    data_handler = HistoricalDataHandler(
        symbol=symbol,
        start=start,
        end=end,
    )
    strategy = SmaCrossStrategy(
        fast_window=fast_window,
        slow_window=slow_window,
    )
    portfolio = NaivePortfolio()
    execution = SimulatedExecutionHandler()

    while True:
        if not events.empty():
            event = events.get()
            if isinstance(event, MarketEvent):
                execution.on_market_event(event)
                strategy.on_market_event(event, events)
            elif isinstance(event, SignalEvent):
                portfolio.on_signal(event, events)
            elif isinstance(event, OrderEvent):
                execution.on_order(event, events)
            elif isinstance(event, FillEvent):
                portfolio.on_fill(event)
        else:
            if data_handler.have_more_bars():
                data_handler.stream_next(events)
            else:
                break

    pos_sma_raw = portfolio.get_position_series()
    prices = data_handler.prices

    # Align event-based positions with the full daily price index and
    # carry forward the last filled position between fills so that
    # returns are computed over actual trading days rather than only on
    # trade dates.
    pos_sma = (
        pos_sma_raw.reindex(prices.index).ffill().fillna(0.0)
    )
    pos_sma = leverage * pos_sma  # apply leverage only to SMA strategy

    pos_bh = build_positions_buy_and_hold(prices)

    trades_bh = count_trades(pos_bh)
    trades_sma = count_trades(pos_sma)

    net_bh = backtest_strategy(
        prices,
        pos_bh,
        cost_rate=cost,
        fin_rate=fin,
    )
    net_sma = backtest_strategy(
        prices,
        pos_sma,
        cost_rate=cost,
        fin_rate=fin,
    )

    # For a fair visual and statistical comparison, restrict both
    # strategies to the period where the SMA rule can actually trade,
    # that is from the first non-zero position onwards.
    first_active = pos_sma[pos_sma != 0.0].index.min()
    if first_active is None:
        first_active = prices.index[0]
    net_bh = net_bh.loc[first_active:]
    net_sma = net_sma.loc[first_active:]

    metrics_bh = compute_metrics(net_bh)
    metrics_sma = compute_metrics(net_sma)

    start_date = first_active.date().isoformat()
    end_date = prices.index[-1].date().isoformat()

    write_tex_macros(
        metrics_bh,
        metrics_sma,
        trades_bh,
        trades_sma,
        symbol,
        fast_window,
        slow_window,
        cost,
        fin,
        leverage,
        start_date,
        end_date,
    )

    plot_equity_curves(net_bh, net_sma, symbol)

    print(f"{symbol} event-based SMA backtest metrics:")
    print("buy-and-hold:")
    pprint(metrics_bh)
    print("event-based SMA strategy:")
    pprint(metrics_sma)


